function linkListeners() {
  $("nav a").on("click", function (e) {
    let pageID = e.currentTarget.id;
    let pageSubject = pageID + "Page";
    pageReload(pageSubject);
    learnMore();
    mimDropDownMenu();
  });
}

function learnMore() {
  $(".undertop a").on("click", function (e) {
    let pageID = e.currentTarget.id;
    let pageSubject = pageID + "Page";
    pageReload(pageSubject);
  });
}

function pageReload(newPage) {
  $("#app").html(eval(newPage));
}

function mimDropDownMenu() {
  let dropDownOpen = false;
  $(".MIM-dropdown a").on("click", function (e) {
    let dropID = e.currentTarget.id;
    let mimDrop = dropID + "Drop";
    if (!dropDownOpen) {
      mimDropReload(mimDrop);
      dropDownOpen = true;
      console.log("hello");
    } else {
      $("#dropdown").empty();
      dropDownOpen = false;
      console.log("bye");
    }
  });
}

function mimDropReload(dropStatus) {
  $("#dropdown").html(eval(dropStatus));
}

$(document).ready(function () {
  pageReload("homePage");
  linkListeners();
  learnMore();
  mimDropDownMenu();
});
